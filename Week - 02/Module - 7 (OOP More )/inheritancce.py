# inheritance provides you "is a" Relation
class Animal:
    pass

#Dog is a animal
class dog(Animal):
    pass
#Tiger is a animal
class Tiger(Animal):
    pass




class Furniture:
    pass


# Chair is a Furniture
class Chair(Furniture):
    pass
#Table is a Furniture
class table(Furniture):
    pass
#Bed is a Furniture
class Bed(Furniture):
    pass 

